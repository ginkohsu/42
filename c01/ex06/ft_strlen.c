#include <unistd.h>

int	ft_strlen(char *str);

int	ft_strlen(char *str)
{
	int	i;
	int	nc;

	i = 0;
	nc = 0;
	while (str[i] != '\0')
	{
		nc ++;
		i ++;
	}
	return (nc);
}
